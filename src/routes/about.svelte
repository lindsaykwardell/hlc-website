    <h3>Healthy Living Family Medicine</h3>
    <em>
      - Providing affordable holistic and integrative primary care to the entire
      family -
    </em>
  </div>
  <p>
    In an ideal world, health insurance would increase the accessibility of
    health care. It would truly be an investment in people’s health.
    Unfortunately, I often hear from patients that this isn’t true.
    Increasingly, people are paying more per month, with higher deductibles and
    fewer benefits. This discourages people from getting the care they need.
  </p>

  <p>
    Additionally several insurance companies have limited the providers allowed
    to be in the network for many of their plans. This means that patients
    either pay full price to stay with their preferred provider, or are forced
    to see new providers who don’t know their history and with whom they don’t
    have a relationship.
  </p>

  <p>
    In this world of increasing health insurance costs, limited insurance
    coverage, and fluctuating economic stability, how do we as a community come
    together to support health and wellness? How do we create a system that
    ensures access to a trusted provider who will take the time to support the
    well-being of our families--while also ensuring the same access to quality
    care for families who do not have the financial means?
  </p>

  <p>
    The transition of Healthy Living Family Medicine to a new care model is my
    answer to these questions.
  </p>

  <p>
    Starting July 1st 2019, Healthy Living Family Medicine will be integrating a
    new model of care that offers a low cost sliding-scale monthly membership
    fee and a “pay what you can” per appointment. At that time, we will no
    longer be contracting with or billing insurance. It is important to me to
    find a way to support my patients outside of the control of the insurance
    companies. Please note, individuals with insurance will still receive their
    standard insurance coverage for all lab testing, imaging, prescriptions and
    referrals to other providers.
  </p>

  <p>
    I believe my patients will find this new model empowering and supportive for
    themselves and their family's health. Patients will be in control and able
    to choose what they spend on their health care and have complete
    transparency for costs as well as the clinic's needs. Patients will not have
    to wonder or wait to find out what insurance may or may not cover. Patients
    will continue to have access to the consistent and comprehensive care that
    Dr. Kerry provides, and also have access to a broad range of new community
    services such as group classes in the studio and our new infrared sauna. I
    believe that this community-based model will be nourishing for my patients'
    holistic health and aligned with their diverse financial needs.
  </p>

  <p>
    Many individuals and families already face a variety of financial
    challenges, and now are being called on to pay more. It is not my intention
    to financially burden you. I trust my patients to do the work necessary to
    clarify how much they can pay, with the hope that they keep in mind that
    there are individuals and families who have less. Those who choose to pay at
    the higher end of the monthly membership cost, or full cost for an
    appointment, will help offset the costs for those who may be unable to
    afford healthcare at this time. I aim to create a model of primary care that
    will allow me to serve my patients in a way that is affordable and
    sustainable. My goal is that this new model will allow patients to feel that
    they are able to make a true and more meaningful investment in their, and
    their community’s, health.
  </p>

  <p>
    Together, we can work to create a sustainable community of wellness for all
    of us here at Healthy Living Family Medicine. Let’s demonstrate to other
    health communities that a different approach that provides superior care is
    possible.
  </p>
